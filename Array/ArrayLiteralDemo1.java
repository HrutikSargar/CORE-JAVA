package Arrays;

public class ArrayLiteralDemo1 {
    public static void main(String[] args) {
        int [] data ={1,2,3,4,5,6};
        System.out.println("Array Length is : "+data.length);
        for (int a=0;a< data.length;a++){
            System.out.println(data[a]);
        }
    }
}
